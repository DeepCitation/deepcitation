/**
 * Build script: compile src/vanilla/runtime/index.ts → minified IIFE.
 *
 * Uses Bun.build() to avoid esbuild platform mismatch in WSL environments.
 *
 * Outputs:
 *   1. src/vanilla/_generated.ts — string constant for embedding (always)
 *   2. lib/vanilla/deepcitation-popover.iife.js — standalone <script> file
 *      (skipped with --generated-only, since tsup's clean:true would delete it)
 */
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");

const generatedOnly = process.argv.includes("--generated-only");

const entryPoint = resolve(ROOT, "src/vanilla/runtime/index.ts");
const generatedPath = resolve(ROOT, "src/vanilla/_generated.ts");
const iifePath = resolve(ROOT, "lib/vanilla/deepcitation-popover.iife.js");

// Build the IIFE bundle using Bun's bundler
const result = await Bun.build({
	entrypoints: [entryPoint],
	format: "iife",
	minify: true,
	target: "browser",
});

if (!result.success) {
	console.error("Build errors:");
	for (const log of result.logs) {
		console.error(log);
	}
	process.exit(1);
}

const iifeCode = (await result.outputs[0].text()).trim();

// 1. Write _generated.ts (string constant for tsup to bundle)
const generatedContent = `// AUTO-GENERATED by scripts/build-vanilla-runtime.mjs — do not edit
export const RUNTIME_JS = ${JSON.stringify(iifeCode)};
`;
writeFileSync(generatedPath, generatedContent, "utf-8");
console.log(`✓ _generated.ts`);

// 2. Write standalone IIFE file to lib/ (unless --generated-only)
if (!generatedOnly) {
	mkdirSync(dirname(iifePath), { recursive: true });
	writeFileSync(iifePath, iifeCode, "utf-8");
	console.log(`✓ deepcitation-popover.iife.js`);
}
