import { execFileSync } from "node:child_process";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const generatedOnly = process.argv.includes("--generated-only");
const entryPoint = resolve(ROOT, "src/vanilla/runtime/index.ts");
const generatedPath = resolve(ROOT, "src/vanilla/_generated.ts");
const iifePath = resolve(ROOT, "lib/vanilla/deepcitation-popover.iife.js");
const result = await Bun.build({ entrypoints: [entryPoint], format: "iife", minify: true, target: "browser" });
if (!result.success) { console.error("Build errors:"); for (const log of result.logs) console.error(log); process.exit(1); }
const iifeCode = (await result.outputs[0].text()).trim();
const generatedContent = `// AUTO-GENERATED by scripts/build-vanilla-runtime.mjs — do not edit\nexport const RUNTIME_JS = ${JSON.stringify(iifeCode)};\n`;
writeFileSync(generatedPath, generatedContent, "utf-8");
console.log("✓ _generated.ts");
if (!generatedOnly) { mkdirSync(dirname(iifePath), { recursive: true }); writeFileSync(iifePath, iifeCode, "utf-8"); console.log("✓ deepcitation-popover.iife.js"); }
if (!generatedOnly) {
  const esbuild = await import("esbuild");
  const cdnEntryPoint = resolve(ROOT, "src/vanilla/runtime/cdn.ts");
  const cdnOutputPath = resolve(ROOT, "lib/vanilla/deepcitation-popover.cdn.js");
  const cdnCssInput = resolve(ROOT, "src/vanilla/runtime/cdn-styles.css");
  const cdnCssTmp = resolve(ROOT, "lib/vanilla/.cdn-popover.css");
  const pkgJson = JSON.parse(readFileSync(resolve(ROOT, "package.json"), "utf-8"));
  console.log("  Extracting Tailwind CSS for CDN bundle...");
  execFileSync("npx", ["@tailwindcss/cli", "-i", cdnCssInput, "-o", cdnCssTmp, "--minify"], { cwd: ROOT, stdio: "pipe" });
  const cdnCss = readFileSync(cdnCssTmp, "utf-8").trim();
  console.log(`  ✓ CDN CSS extracted (${(cdnCss.length / 1024).toFixed(1)}KB)`);
  const cdnResult = await esbuild.build({
    entryPoints: [cdnEntryPoint], bundle: true, format: "iife", globalName: "__dc_cdn__", minify: true, target: ["es2020"], write: false,
    alias: { react: "preact/compat", "react-dom": "preact/compat", "react/jsx-runtime": "preact/jsx-runtime" },
    jsx: "automatic", jsxImportSource: "preact",
    define: { __CDN_CSS__: JSON.stringify(cdnCss), "process.env.NODE_ENV": '"production"' },
  });
  if (cdnResult.errors.length > 0) { console.error("CDN build errors:", cdnResult.errors); process.exit(1); }
  let cdnCode = new TextDecoder().decode(cdnResult.outputFiles[0].contents);
  cdnCode = cdnCode.replace(/__VERSION__/g, pkgJson.version);
  mkdirSync(dirname(cdnOutputPath), { recursive: true });
  writeFileSync(cdnOutputPath, cdnCode, "utf-8");
  console.log(`✓ deepcitation-popover.cdn.js (${(cdnCode.length / 1024).toFixed(1)}KB)`);
}
